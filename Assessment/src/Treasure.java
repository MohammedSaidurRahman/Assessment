
public class Treasure {
	
	private double treasureY;
	
	private double treasureX;
	

	public Treasure(double x, double y) {
		this.treasureX = x;
		this.treasureY = y;
	}

	public double getTreasureY() {
		return treasureY;
	}

	public void setTreasureY(double treasureY) {
		this.treasureY = treasureY;
	}

	public double getTreasureX() {
		return treasureX;
	}

	public void setTreasureX(double treasureX) {
		this.treasureX = treasureX;
	}
	
	

}
