
public class Map {
	
	private int mapY;
	
	private int mapX;
	
	public Map(int mapY, int mapX)
	{
		this.mapY = mapY;
		this.mapX = mapX;
	}

	public int getMapY() {
		return mapY;
	}

	public void setMapY(int mapY) {
		this.mapY = mapY;
	}

	public int getMapX() {
		return mapX;
	}

	public void setMapX(int mapX) {
		this.mapX = mapX;
	}
	
	
	

}
	

